#include<iostream>
using namespace std;
int main(){

    int num1,num2;
    cout<<"First number : "<<endl;
    cin>>num1;
    cout<<"Second number : "<<endl;
    cin>>num2;

    cout<<"Sum : "<<(num1 + num2)<<endl;
    cout<<"Differnece : "<<(num1 - num2)<<endl;
    cout<<"Product : "<<(num1 * num2)<<endl;
    cout<<"Division : "<<(num1 / num2)<<endl;
    cout<<"Modulus : "<<(num1 % num2)<<endl;

    return 0;
}